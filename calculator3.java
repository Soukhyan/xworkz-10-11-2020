class calculator3
{
     public static int add()
     {
     return 10+20;
     }
  public static int subtract()
     {
     return 10-20;
     }
  public static int multiply()
     {
     return 10*20;
     }
  public static int division()
     {
     return 10/20;
      }
   public static void main(String args[])
     {
      int sum=calculator3.add();
System.out.println("sum of two numbers ="+sum);
      int sub=calculator3.subtract();
System.out.println("subtraction of two numbers ="+sub);
      int multi=calculator3.multiply();
System.out.println("product of two numbers ="+multi);
      int divide=calculator3.division();
System.out.println("division of two numbers ="+divide);
     }
  
}